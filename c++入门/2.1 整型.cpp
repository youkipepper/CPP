#include <iostream>
using namespace std;

int main()
{
    //整型
    // 1、短整型(-32768~32767)
    short num1 = 32768;
    // 2、整型
    int num2 = 10;
    // 3、长整型
    long num3 = 10;
    // 4、长长整型
    long long nmm4 = 10;

    cout << "nmb1=" << num1 << endl;
    cout << "nmb2=" << num2 << endl;
    cout << "nmb3=" << num3 << endl;
    cout << "nmb4=" << nmm4 << endl;

    system("pause");

    return 0;
}
