#include <iostream>
using namespace std;

int main()
{
    //转义字符

    //换行符 \n

    cout << "hello world\n";

    //反斜杠
    cout << "\\" << endl;

    //水平制表符 \t 作用可以整齐输出字符

    cout << "aaaa\thelloworld" << endl;
    cout << "aa\thelloworld" << endl;
    cout << "aaaa\thelloworld" << endl;

    system("pause");

    return 0;
}
